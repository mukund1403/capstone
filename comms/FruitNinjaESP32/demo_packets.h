// Helper function to add float arrays to JSON
void addFloatArray(JsonDocument &doc, const char *key, const float *arr, int len) {
    JsonArray a = doc.createNestedArray(key);
    for (int i = 0; i < len; i++) {
        a.add(arr[i]);
    }
}
