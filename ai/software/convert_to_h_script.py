import os
import numpy as np
import tensorflow as tf

BN_EPSILON = 1e-3   # Must match what Keras used during training (default 1e-3)

script_dir  = os.path.dirname(os.path.abspath(__file__))
model_path  = os.path.join(script_dir, 'best_live_model.h5')
output_path = os.path.join(script_dir, 'weights.h')

model = tf.keras.models.load_model(model_path)

def array_to_c(arr: np.ndarray, var_name: str, comment: str) -> str:
    """Flatten array and emit as a const float C array."""
    flat = arr.flatten().astype(np.float32)
    vals = ", ".join(f"{v:.8f}f" for v in flat)
    return f"// {comment}\nconst float {var_name}[] = {{{vals}}};\n\n"

def fold_batchnorm(gamma, beta, mean, variance, eps=BN_EPSILON):
    """
    Fold BN params into scale+bias so HLS does:
        output = scale * conv_out + bias
    then ReLU — matching the Keras BN+activation order exactly.
    """
    scale = gamma / np.sqrt(variance + eps)
    bias  = beta - mean * scale
    return scale.astype(np.float32), bias.astype(np.float32)

with open(output_path, 'w') as f:
    f.write("#ifndef WEIGHTS_H\n")
    f.write("#define WEIGHTS_H\n\n")
    f.write("// Auto-generated by export_weights.py\n")
    f.write("// BatchNorm parameters are PRE-FOLDED into scale+bias.\n")
    f.write("// DO NOT replace with raw Keras BN params — the HLS will give wrong results.\n\n")

    for layer in model.layers:
        raw_weights = layer.get_weights()
        if not raw_weights:
            continue  # Dropout, Flatten, Input, Pooling — no weights

        lname = layer.name
        safe  = lname.replace("/", "_").replace(":", "_")
        ltype = type(layer).__name__

        print(f"Exporting: {lname}  ({ltype})  —  {len(raw_weights)} raw param(s)")

        if ltype == 'BatchNormalization':
            # Keras order: [gamma, beta, moving_mean, moving_variance]
            if len(raw_weights) != 4:
                raise ValueError(f"Expected 4 BN params for {lname}, got {len(raw_weights)}")
            gamma, beta, mean, variance = raw_weights
            scale, bias = fold_batchnorm(gamma, beta, mean, variance)

            f.write(array_to_c(scale, f"{safe}_param_0",
                                f"Layer: {lname}  |  FOLDED scale = gamma/sqrt(var+eps)"))
            f.write(array_to_c(bias,  f"{safe}_param_1",
                                f"Layer: {lname}  |  FOLDED bias  = beta - mean*scale"))

            print(f"  → Folded BN: scale range [{scale.min():.4f}, {scale.max():.4f}]")
            print(f"               bias  range [{bias.min():.4f},  {bias.max():.4f}]")

        else:
            # Conv1D, Dense: param_0=kernel, param_1=bias (standard)
            for i, w in enumerate(raw_weights):
                param_label = ["kernel", "bias"][i] if i < 2 else f"param_{i}"
                f.write(array_to_c(w, f"{safe}_param_{i}",
                                   f"Layer: {lname}  |  {param_label}  shape={list(w.shape)}"))
                print(f"  → {param_label}: shape={list(w.shape)}")

    f.write("#endif  // WEIGHTS_H\n")

print(f"\nDone. weights.h written to:\n  {output_path}")
print("\nVerification checklist:")
print("  1. BN param_0 = folded scale  (NOT raw gamma)")
print("  2. BN param_1 = folded bias   (NOT raw beta)")
print("  3. Conv kernel shape: [kernel_size, in_ch, out_ch] — matches conv_index() in HLS")
print("  4. Dense kernel shape: [in_dim, out_dim]           — matches dense_index() in HLS")
